SEED = 42
random.seed(SEED)
np.random.seed(SEED)
torch.manual_seed(SEED)
DEVICE = "cuda" if torch.cuda.is_available() else "cpu"
print(f"Using device: {DEVICE}")

### -------------------------------
### Problem setup (ODE: y'=-k y, y(0)=y0)
### -------------------------------
k, y0 = 2.0, 1.5
T0, T1 = 0.0, 1.0
N_TRAIN = 100
N_COL   = 256

def exact_np(t):
    return y0 * np.exp(-k * t)

##### Eval grid
t_eval_np = np.linspace(T0, T1, 200)
t_eval = torch.tensor(t_eval_np, dtype=torch.float32, device=DEVICE).reshape(-1,1)
y_true_np = exact_np(t_eval_np)

### -------------------------------
### (IMPULSE NOISE / OUTLIERS)
### -------------------------------
CORRUPTION_RATE = 0.0
IMPULSE_FACTOR  = 0.0

### -------------------------------
### Hyperparameters
### -------------------------------
MAX_EPOCHS = 1000
PATIENCE   = 20
MIN_DELTA  = 1e-4

HIDDEN_LAYERS = 2
HIDDEN_WIDTH  = 64
ACT           = "tanh"

LR = 1e-3
WD = 0.0

LAMBDA_IC   = 1.0
LAMBDA_PDE  = 1.0
LAMBDA_DATA = 1.0

P_RANGE = (0.2, 0.5)
T_MC    = 1000
M_BAG   = 20

### -------------------------------
### Data generator (DYNAMIC IMPULSE NOISE)
### -------------------------------
def make_dataset():
    t_np = np.linspace(T0, T1, N_TRAIN)
    y_clean = exact_np(t_np)
    y_np = y_clean.copy()

    if CORRUPTION_RATE > 0:
        max_y = np.max(np.abs(y_clean))
        actual_impulse_mag = IMPULSE_FACTOR * max_y

        n_corrupt = int(N_TRAIN * CORRUPTION_RATE)
        corrupt_idx = np.random.choice(N_TRAIN, n_corrupt, replace=False)
        signs = np.random.choice([-1.0, 1.0], size=n_corrupt)
        y_np[corrupt_idx] = signs * actual_impulse_mag

    t = torch.tensor(t_np, dtype=torch.float32, device=DEVICE).reshape(-1,1)
    y = torch.tensor(y_np, dtype=torch.float32, device=DEVICE).reshape(-1,1)
    return t, y

IC_t = torch.zeros((1,1), dtype=torch.float32, device=DEVICE)
IC_y = torch.tensor([[y0]], dtype=torch.float32, device=DEVICE)

def bootstrap(t, y):
    n = len(t)
    idx = np.random.choice(n, size=n, replace=True)
    return t[idx], y[idx]

def make_collocation_fixed(n_col=N_COL):
    return (torch.rand((n_col,1), device=DEVICE) * (T1 - T0) + T0)

### -------------------------------
### Models & UQ
### -------------------------------
def act(name):
    return {"tanh": nn.Tanh(), "relu": nn.ReLU(), "gelu": nn.GELU(), "silu": nn.SiLU()}[name]

class MLP(nn.Module):
    def __init__(self, in_dim=1, out_dim=1, L=HIDDEN_LAYERS, W=HIDDEN_WIDTH, act_name=ACT):
        super().__init__()
        A = act(act_name)
        layers = [nn.Linear(in_dim, W), A]
        for _ in range(L-1):
            layers += [nn.Linear(W, W), A]
        layers += [nn.Linear(W, out_dim)]
        self.net = nn.Sequential(*layers)

    def forward(self, x):
        return self.net(x)

class TestTimeBernoulliUQ(nn.Module):
    def __init__(self, core: nn.Sequential):
        super().__init__()
        self.core = core
        self.p = 0.3

    def set_p(self, p):
        self.p = float(p)

    def forward(self, x):
        out = x
        for m in self.core:
            out = m(out)
            if isinstance(m, (nn.Tanh, nn.ReLU, nn.GELU, nn.SiLU)):
                msk = (torch.rand_like(out) > self.p).float()
                out = out * msk / (1.0 - self.p + 1e-12)
        return out

### -------------------------------
### Losses & EarlyStopping
### -------------------------------
mse = nn.MSELoss()

def nn_loss(model, t, y):
    return mse(model(t), y)

def pinn_residual(model, t):
    t.requires_grad_(True)
    y = model(t)
    dy = autograd.grad(y, t, torch.ones_like(y), create_graph=True)[0]
    return dy + k * y

def pinn_total_loss_fixed(model, t, y, t_col_fixed):
    l_data = mse(model(t), y)
    l_ic   = mse(model(IC_t), IC_y)
    res    = pinn_residual(model, t_col_fixed.requires_grad_(True))
    l_pde  = torch.mean(res**2)
    return LAMBDA_DATA * l_data + LAMBDA_IC * l_ic + LAMBDA_PDE * l_pde

class ES:
    def __init__(self, patience=PATIENCE, min_delta=MIN_DELTA):
        self.p = patience; self.d = min_delta; self.best = None; self.bad = 0

    def step(self, v):
        if self.best is None or v < self.best - self.d:
            self.best = v; self.bad = 0
        else:
            self.bad += 1
        return self.bad >= self.p

