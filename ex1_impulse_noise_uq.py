### Training Backbones
### -------------------------------
def train_backbone_ann(model, t_train, y_train):
    model = model.to(DEVICE)
    opt = optim.Adam(model.parameters(), lr=LR, weight_decay=WD)
    es  = ES(); best, bestv = None, float("inf")
    for ep in range(1, MAX_EPOCHS+1):
        model.train(); opt.zero_grad()
        loss = nn_loss(model, t_train, y_train)
        loss.backward(); opt.step()
        v = float(loss.item())
        if v < bestv - MIN_DELTA:
            bestv = v; es.bad = 0; best = {k: w.detach().cpu().clone() for k, w in model.state_dict().items()}
        elif es.step(v): break
    if best: model.load_state_dict(best)
    return model.eval()

def train_backbone_pinn_fixed_col(model, t_train, y_train, t_col_fixed):
    model = model.to(DEVICE)
    opt = optim.Adam(model.parameters(), lr=LR, weight_decay=WD)
    es  = ES(); best, bestv = None, float("inf")
    for ep in range(1, MAX_EPOCHS+1):
        model.train(); opt.zero_grad()
        loss = pinn_total_loss_fixed(model, t_train, y_train, t_col_fixed)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        opt.step()
        v = float(loss.item())
        if v < bestv - MIN_DELTA:
            bestv = v; es.bad = 0; best = {k: w.detach().cpu().clone() for k, w in model.state_dict().items()}
        elif es.step(v): break
    if best: model.load_state_dict(best)
    return model.eval()

### -------------------------------
### Predictions (Single & Bagging)
### -------------------------------
@torch.no_grad()
def predict_deterministic(model):
    return model(t_eval).cpu().numpy().ravel()

def predict_uq_single(trained_model):
    uq = TestTimeBernoulliUQ(trained_model.net).to(DEVICE)
    samples = []
    for _ in range(T_MC):
        uq.set_p(random.uniform(*P_RANGE))
        with torch.no_grad():
            samples.append(uq(t_eval).cpu().numpy().ravel())
    S = np.stack(samples, 0)
    return S.mean(0), S.std(0, ddof=0)

@torch.no_grad()
def bagging_predict_deterministic(members):
    preds = [m(t_eval).cpu().numpy().ravel() for m in members]
    return np.mean(np.stack(preds, 0), 0)

def predict_uq_bagging(members):
    uq_members = [TestTimeBernoulliUQ(m.net).to(DEVICE) for m in members]
    bagged_samples = []
    for _ in range(T_MC):
        preds_m = []
        for uq in uq_members:
            uq.set_p(random.uniform(*P_RANGE))
            with torch.no_grad():
                preds_m.append(uq(t_eval).cpu().numpy().ravel())
        bagged_samples.append(np.mean(np.stack(preds_m, 0), axis=0))
    S = np.stack(bagged_samples, 0)
    return S.mean(0), S.std(0, ddof=0)

### -------------------------------
### Metrics
### -------------------------------
def mae(yhat, y): return float(np.mean(np.abs(yhat - y)))
def mse_np(yhat, y): return float(np.mean((yhat - y)**2))
def rmse(yhat, y): return float(np.sqrt(mse_np(yhat, y)))
def picp(mu, sd, y_true):
    lo, hi = mu - 1.96 * sd, mu + 1.96 * sd
    yt = y_true.ravel()
    return float(np.mean((yt >= lo) & (yt <= hi))) * 100  # Tính theo %

def mpiw(sd):
    return 2 * 1.96 * float(np.mean(sd))

