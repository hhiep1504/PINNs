import os
os.environ["TORCH_DISABLE_DYNAMO"] = "1"

import numpy as np, random, math
import torch, torch.nn as nn
import torch.optim as optim
import torch.autograd as autograd
import matplotlib.pyplot as plt
import os, math, random, time

# -------------------------------
# Repro & device
# -------------------------------
SEED = 42
random.seed(SEED); np.random.seed(SEED); torch.manual_seed(SEED)
DEVICE = "cuda" if torch.cuda.is_available() else "cpu"
print(f"Using device: {DEVICE}")

### -------------------------------

k, y0 = 2.0, 1.5
T0, T1 = 0.0, 1.0
N_TRAIN = 100
N_COL   = 256

CORRUPTION_RATE = 0.05
IMPULSE_FACTOR  = 3

def exact_np(t):
    return y0 * np.exp(-k * t)

t_eval_np = np.linspace(T0, T1, 200)
t_eval = torch.tensor(t_eval_np, dtype=torch.float32, device=DEVICE).reshape(-1,1)
y_true_np = exact_np(t_eval_np)

def make_dataset():
    t_np = np.linspace(T0, T1, N_TRAIN)
    y_clean = exact_np(t_np)
    y_np = y_clean.copy()

    if CORRUPTION_RATE > 0:
        max_y = np.max(np.abs(y_clean))
        actual_impulse_mag = IMPULSE_FACTOR * max_y

        n_corrupt = int(N_TRAIN * CORRUPTION_RATE)
        corrupt_idx = np.random.choice(N_TRAIN, n_corrupt, replace=False)
        signs = np.random.choice([-1.0, 1.0], size=n_corrupt)
        y_np[corrupt_idx] = signs * actual_impulse_mag

    t = torch.tensor(t_np, dtype=torch.float32, device=DEVICE).reshape(-1,1)
    y = torch.tensor(y_np, dtype=torch.float32, device=DEVICE).reshape(-1,1)
    return t, y

IC_t = torch.zeros((1,1), dtype=torch.float32, device=DEVICE)
IC_y = torch.tensor([[y0]], dtype=torch.float32, device=DEVICE)

def bootstrap(t, y):
    n = len(t)
    idx = np.random.choice(n, size=n, replace=True)
    return t[idx], y[idx]

def make_collocation_fixed(n_col=N_COL):
    return (torch.rand((n_col,1), device=DEVICE) * (T1 - T0) + T0)


### -------------------------------
MAX_EPOCHS = 1000
PATIENCE   = 20
MIN_DELTA  = 1e-4
LR = 1e-3
WD = 0.0
LAMBDA_IC = 1.0; LAMBDA_PDE = 1.0; LAMBDA_DATA = 1.0

# MC-Dropout for UQ
P_RANGE = (0.2, 0.5)
T_MC    = 1000

class MLP(nn.Module):
    def __init__(self, in_dim=1, out_dim=1):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(in_dim, 64), nn.Tanh(),
            nn.Linear(64, 64), nn.Tanh(),
            nn.Linear(64, out_dim)
        )
    def forward(self, x): return self.net(x)

class TestTimeBernoulliUQ(nn.Module):
    def __init__(self, core: nn.Sequential):
        super().__init__()
        self.core = core
        self.p = 0.3

    def set_p(self, p):
        self.p = float(p)

    def forward(self, x):
        out = x
        for m in self.core:
            out = m(out)
            if isinstance(m, (nn.Tanh, nn.ReLU, nn.GELU, nn.SiLU)):
                msk = (torch.rand_like(out) > self.p).float()
                out = out * msk / (1.0 - self.p + 1e-12)
        return out

mse = nn.MSELoss()

def pinn_residual(model, t):
    t.requires_grad_(True)
    y = model(t)
    dy = autograd.grad(y, t, torch.ones_like(y), create_graph=True)[0]
    return dy + k * y

def pinn_total_loss_fixed(model, t, y, t_col_fixed):
    l_data = mse(model(t), y)
    l_ic   = mse(model(IC_t), IC_y)
    res    = pinn_residual(model, t_col_fixed.requires_grad_(True))
    l_pde  = torch.mean(res**2)
    return LAMBDA_DATA * l_data + LAMBDA_IC * l_ic + LAMBDA_PDE * l_pde

class ES:
    def __init__(self):
        self.best = None; self.bad = 0
    def step(self, v):
        if self.best is None or v < self.best - MIN_DELTA:
            self.best = v; self.bad = 0
        else:
            self.bad += 1
        return self.bad >= PATIENCE

def train_member(t_train, y_train, t_col):
    model = MLP().to(DEVICE)
    opt = optim.Adam(model.parameters(), lr=LR, weight_decay=WD)
    es = ES(); best_state = None; best_v = float("inf")
    for _ in range(MAX_EPOCHS):
        model.train(); opt.zero_grad()
        loss = pinn_total_loss_fixed(model, t_train, y_train, t_col)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        opt.step()

        v = float(loss.item())
        if v < best_v - MIN_DELTA:
            best_v = v; es.bad = 0; best_state = {k: w.detach().cpu().clone() for k, w in model.state_dict().items()}
        elif es.step(v): break
    if best_state: model.load_state_dict(best_state)
    return model.eval()

