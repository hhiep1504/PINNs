@torch.no_grad()
def predict_bagging(members):
    preds = [m(t_eval).cpu().numpy().ravel() for m in members]
    return np.mean(np.stack(preds, 0), 0)

def predict_uq_bagging(members):
    uq_members = [TestTimeBernoulliUQ(m.net).to(DEVICE) for m in members]
    bagged_samples = []
    for _ in range(T_MC):
        preds_m = []
        for uq in uq_members:
            uq.set_p(random.uniform(*P_RANGE))
            with torch.no_grad():
                preds_m.append(uq(t_eval).cpu().numpy().ravel())
        bagged_samples.append(np.mean(np.stack(preds_m, 0), axis=0))
    S = np.stack(bagged_samples, 0)
    return S.mean(0), S.std(0, ddof=0)

# UQ 95% CI
def picp(mu, sd, y_true):
    lo, hi = mu - 1.96 * sd, mu + 1.96 * sd
    yt = y_true.ravel()
    return float(np.mean((yt >= lo) & (yt <= hi))) * 100

def mpiw(sd):
    return 2 * 1.96 * float(np.mean(sd))

def main():
    t_train, y_train = make_dataset()
    t_train_np = t_train.cpu().numpy()
    y_train_np = y_train.cpu().numpy()

    M_LIST = [1,2,5,10,20,50,100,200]
    bagging_members = []
    total_time = 0.0

    print("=== KHẢO SÁT ĐỘ NHẠY M - BAGGING PINN (VÍ DỤ 1) ===")
    print(f"{'M':<5} | {'Thời gian(s)':<13} | {'MAE (Det)':<10} | {'RMSE (Det)':<11} | {'PICP (%)':<10} | {'MPIW'}")
    print("-" * 80)

    for target_m in M_LIST:
        start_time = time.time()

        while len(bagging_members) < target_m:
            t_b, y_b = bootstrap(t_train_np, y_train_np)
            t_b_t = torch.tensor(t_b, dtype=torch.float32, device=DEVICE)
            y_b_t = torch.tensor(y_b, dtype=torch.float32, device=DEVICE)
            t_col_b = make_collocation_fixed()

            trained_model = train_member(t_b_t, y_b_t, t_col_b)
            bagging_members.append(trained_model)


        total_time += (time.time() - start_time)

        #  Deterministic
        y_pred_det = predict_bagging(bagging_members)
        mae = float(np.mean(np.abs(y_pred_det - y_true_np)))
        rmse_det = float(np.sqrt(np.mean((y_pred_det - y_true_np)**2)))

        # PICP và MPIW
        y_mean_uq, y_sd_uq = predict_uq_bagging(bagging_members)
        cr_val = picp(y_mean_uq, y_sd_uq, y_true_np)
        mpiw_val = mpiw(y_sd_uq)

        print(f"{target_m:<5} | {total_time:<13.2f} | {mae:<10.6f} | {rmse_det:<11.6f} | {cr_val:>6.2f}     | {mpiw_val:.6f}")

if __name__ == "__main__":
    main()

