def train_ann(model, Xsup, ysup, Xva, yva):
    model = model.to(DEVICE)
    xt_tr, u_tr = to_torch_xy(Xsup, ysup)
    xt_va, u_va = to_torch_xy(Xva, yva)
    opt = optim.Adam(model.parameters(), lr=LR, weight_decay=WD)
    es  = ES(); best, bestv = None, float("inf")

    for ep in range(1, MAX_EPOCHS+1):
        model.train(); opt.zero_grad()
        loss = ann_loss(model, xt_tr, u_tr)
        loss.backward(); opt.step()

        model.eval()
        with torch.no_grad():
            v = float(mse(model(xt_va), u_va).item())
            if v < bestv - MIN_DELTA:
                bestv = v; es.bad = 0; best = {k:w.detach().cpu().clone() for k,w in model.state_dict().items()}
            elif es.step(v): break
    if best is not None: model.load_state_dict(best)
    return model.eval()

def train_pinn_fixed_col(model, Xsup, ysup, Xva, yva, x_c_fixed, t_c_fixed):
    model = model.to(DEVICE)
    xt_tr, u_tr = to_torch_xy(Xsup, ysup)
    xt_va, u_va = to_torch_xy(Xva, yva)
    opt = optim.Adam(model.parameters(), lr=LR, weight_decay=WD)
    es  = ES(); best, bestv = None, float("inf")

    for ep in range(1, MAX_EPOCHS+1):
        model.train(); opt.zero_grad()
        loss = pinn_total_loss_fixed(model, xt_tr, u_tr, x_c_fixed, t_c_fixed)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        opt.step()

        model.eval()
        with torch.no_grad():
            v = float(mse(model(xt_va), u_va).item())
            if v < bestv - MIN_DELTA:
                bestv = v; es.bad = 0; best = {k:w.detach().cpu().clone() for k,w in model.state_dict().items()}
            elif es.step(v): break
    if best is not None: model.load_state_dict(best)
    return model.eval()

