def act(name):
    return {"tanh": nn.Tanh(), "relu": nn.ReLU(), "gelu": nn.GELU(), "silu": nn.SiLU()}[name]

class MLP(nn.Module):
    def __init__(self, in_dim=2, out_dim=1, L=HIDDEN_LAYERS, W=HIDDEN_WIDTH, act_name=ACT):
        super().__init__()
        A = act(act_name)
        layers = [nn.Linear(in_dim, W), A]
        for _ in range(L-1):
            layers += [nn.Linear(W, W), A]
        layers += [nn.Linear(W, out_dim)]
        self.net = nn.Sequential(*layers)

    def forward(self, xt):
        return self.net(xt)

class TestTimeBernoulliUQ(nn.Module):
    def __init__(self, core_sequential: nn.Sequential):
        super().__init__()
        self.core = core_sequential
        self.p = 0.3

    def set_p(self, p: float):
        self.p = float(p)

    def forward(self, xt):
        out = xt
        for m in self.core:
            out = m(out)
            if isinstance(m, (nn.Tanh, nn.ReLU, nn.GELU, nn.SiLU)):
                mask = (torch.rand_like(out) > self.p).float()
                out  = out * mask / (1.0 - self.p + 1e-12)
        return out

### -------------------------------
### Losses & residual (wave: u_tt - u_xx)
### -------------------------------
mse = nn.MSELoss()

def ann_loss(model, xt, u): return mse(model(xt), u)

def wave_residual(model, x, t):
    xt = torch.cat([x,t], dim=1)
    xt.requires_grad_(True)
    u  = model(xt)
    du = autograd.grad(u, xt, torch.ones_like(u), create_graph=True)[0]
    u_x = du[:,0:1]
    u_t = du[:,1:2]
    u_xx = autograd.grad(u_x, xt, torch.ones_like(u_x), create_graph=True)[0][:,0:1]
    u_tt = autograd.grad(u_t, xt, torch.ones_like(u_t), create_graph=True)[0][:,1:2]
    return u_tt - u_xx

def pinn_total_loss_fixed(model, xt_sup, u_sup, x_c_fixed, t_c_fixed):
    l_data = mse(model(xt_sup), u_sup)
    res    = wave_residual(model, x_c_fixed, t_c_fixed)
    l_pde  = torch.mean(res**2)
    return LAMBDA_DATA * l_data + LAMBDA_PDE * l_pde

### -------------------------------
### EarlyStopping & Helpers
### -------------------------------
class ES:
    def __init__(self, patience=PATIENCE, min_delta=MIN_DELTA):
        self.p = patience; self.d = min_delta; self.best = None; self.bad = 0
    def step(self, v):
        if self.best is None or v < self.best - self.d:
            self.best = v; self.bad = 0
        else:
            self.bad += 1
        return self.bad >= self.p

def to_torch_xy(X, y):
    xt = torch.tensor(X, dtype=torch.float32, device=DEVICE)
    u  = torch.tensor(y, dtype=torch.float32, device=DEVICE)
    return xt, u

