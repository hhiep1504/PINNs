@torch.no_grad()
def predict_det(model, X):
    xt = torch.tensor(X, dtype=torch.float32, device=DEVICE)
    return model(xt).cpu().numpy().ravel()

@torch.no_grad()
def predict_det_bagging(members, X):
    preds = [predict_det(m, X) for m in members]
    return np.mean(np.stack(preds, 0), 0)

def mae_np(yhat, ytrue): return float(np.mean(np.abs(yhat - ytrue.ravel())))
def mse_np(yhat, ytrue): return float(np.mean((yhat - ytrue.ravel())**2))
def rmse_np(yhat, ytrue): return float(np.sqrt(mse_np(yhat, ytrue)))
def picp(mu, sd, y_true):
    lo, hi = mu - 1.96 * sd, mu + 1.96 * sd
    yt = y_true.ravel()
    return float(np.mean((yt >= lo) & (yt <= hi))) * 100  # Tính theo %

def mpiw(sd):
    return 2 * 1.96 * float(np.mean(sd))

def predict_uq_single(model, X_np):
    Xt = torch.tensor(X_np, dtype=torch.float32, device=DEVICE)
    uq = TestTimeBernoulliUQ(model.net).to(DEVICE)
    samples = []
    for _ in range(T_MC):
        uq.set_p(random.uniform(*P_RANGE_TEST))
        with torch.no_grad():
            samples.append(uq(Xt).cpu().numpy().ravel())
    S = np.stack(samples, 0)
    return S.mean(0), S.std(0, ddof=0)

def predict_uq_bagging(members, X_np):
    Xt = torch.tensor(X_np, dtype=torch.float32, device=DEVICE)
    uq_members = [TestTimeBernoulliUQ(m.net).to(DEVICE) for m in members]
    bagged = []
    for _ in range(T_MC):
        preds_m = []
        for uq in uq_members:
            uq.set_p(random.uniform(*P_RANGE_TEST))
            with torch.no_grad():
                preds_m.append(uq(Xt).cpu().numpy().ravel())
        bagged.append(np.mean(np.stack(preds_m, 0), 0))
    S = np.stack(bagged, 0)
    return S.mean(0), S.std(0, ddof=0)

