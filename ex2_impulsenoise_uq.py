HIDDEN_LAYERS = 3
HIDDEN_WIDTH  = 128
ACT           = "tanh"

MAX_EPOCHS = 1000
PATIENCE   = 20
MIN_DELTA  = 1e-4
LR = 1e-3
WD = 0.0

LAMBDA_DATA = 1.0
LAMBDA_PDE  = 0.1
