MAX_EPOCHS = 1000
PATIENCE   = 20
MIN_DELTA  = 1e-4
LR = 1e-3
LAMBDA_DATA = 1.0; LAMBDA_PDE = 0.1

P_RANGE_TEST = (0.2, 0.5)
T_MC         = 1000

class MLP(nn.Module):
    def __init__(self, in_dim=2, out_dim=1):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(in_dim, 128), nn.Tanh(),
            nn.Linear(128, 128), nn.Tanh(),
            nn.Linear(128, 128), nn.Tanh(),
            nn.Linear(128, out_dim)
        )
    def forward(self, xt): return self.net(xt)

class TestTimeBernoulliUQ(nn.Module):
    def __init__(self, core_sequential: nn.Sequential):
        super().__init__()
        self.core = core_sequential
        self.p = 0.3

    def set_p(self, p: float):
        self.p = float(p)

    def forward(self, xt):
        out = xt
        for m in self.core:
            out = m(out)
            if isinstance(m, (nn.Tanh, nn.ReLU, nn.GELU, nn.SiLU)):
                mask = (torch.rand_like(out) > self.p).float()
                out  = out * mask / (1.0 - self.p + 1e-12)
        return out

mse = nn.MSELoss()

def wave_residual(model, x, t):
    xt = torch.cat([x,t], dim=1); xt.requires_grad_(True)
    u  = model(xt)
    du = autograd.grad(u, xt, torch.ones_like(u), create_graph=True)[0]
    u_x = du[:,0:1]; u_t = du[:,1:2]
    u_xx = autograd.grad(u_x, xt, torch.ones_like(u_x), create_graph=True)[0][:,0:1]
    u_tt = autograd.grad(u_t, xt, torch.ones_like(u_t), create_graph=True)[0][:,1:2]
    return u_tt - u_xx

def pinn_total_loss_fixed(model, xt_sup, u_sup, x_c, t_c):
    l_data = mse(model(xt_sup), u_sup)
    res    = wave_residual(model, x_c, t_c)
    l_pde  = torch.mean(res**2)
    return LAMBDA_DATA * l_data + LAMBDA_PDE * l_pde

class ES:
    def __init__(self):
        self.best = None; self.bad = 0
    def step(self, v):
        if self.best is None or v < self.best - MIN_DELTA:
            self.best = v; self.bad = 0
        else: self.bad += 1
        return self.bad >= PATIENCE

def train_member(Xsup, ysup, Xva, yva, x_c, t_c):
    model = MLP().to(DEVICE)
    xt_tr = torch.tensor(Xsup, dtype=torch.float32, device=DEVICE)
    u_tr  = torch.tensor(ysup, dtype=torch.float32, device=DEVICE)
    xt_va = torch.tensor(Xva, dtype=torch.float32, device=DEVICE)
    u_va  = torch.tensor(yva, dtype=torch.float32, device=DEVICE)

    opt = optim.Adam(model.parameters(), lr=LR)
    es = ES(); best_state = None; best_v = float("inf")

    for _ in range(1, MAX_EPOCHS + 1):
        model.train(); opt.zero_grad()
        loss = pinn_total_loss_fixed(model, xt_tr, u_tr, x_c, t_c)
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        opt.step()

        model.eval()
        with torch.no_grad():
            v = float(mse(model(xt_va), u_va).item())
            if v < best_v - MIN_DELTA:
                best_v = v; es.bad = 0; best_state = {k:w.detach().cpu().clone() for k,w in model.state_dict().items()}
            elif es.step(v): break
    if best_state is not None: model.load_state_dict(best_state)
    return model.eval()

