CORRUPTION_RATE = .05   # Level
IMPULSE_FACTOR  = 3    # Factor

def u_exact_np(x, t):
    return np.sin(np.pi * x) * np.cos(np.pi * t)

def make_supervised_grid_dynamic_impulse(nx=20, nt=10):
    xs = np.linspace(0, 1, nx)
    ts = np.linspace(0, 1, nt)
    Xg, Tg = np.meshgrid(xs, ts, indexing="xy")
    X = np.c_[Xg.ravel(), Tg.ravel()].astype(np.float32)

    u_clean = u_exact_np(X[:,0:1], X[:,1:2])
    y_np = u_clean.copy()

    n_total = len(X)
    if CORRUPTION_RATE > 0:
        max_y = np.max(np.abs(u_clean))
        actual_impulse_mag = IMPULSE_FACTOR * max_y

        n_corrupt = int(n_total * CORRUPTION_RATE)
        corrupt_idx = np.random.choice(n_total, n_corrupt, replace=False)

        signs = np.random.choice([-1.0, 1.0], size=(n_corrupt, 1))
        y_np[corrupt_idx] = signs * actual_impulse_mag

    return X, y_np.astype(np.float32)

def make_clean_test_grid(nx=60, nt=60):
    xs = np.linspace(0, 1, nx)
    ts = np.linspace(0, 1, nt)
    Xg, Tg = np.meshgrid(xs, ts, indexing="xy")
    grid = np.c_[Xg.ravel(), Tg.ravel()].astype(np.float32)
    y_exact = u_exact_np(grid[:,0:1], grid[:,1:2]).ravel()
    return xs, ts, Xg, Tg, grid, y_exact

def bootstrap(X, y):
    n = len(X)
    idx = np.random.choice(n, size=n, replace=True)
    return X[idx], y[idx]

def make_collocation_fixed(n_col=1000, jitter=0.0):
    def cheb_nodes_1d(n):
        k = np.arange(1, n+1)
        return 0.5 * (1 - np.cos(np.pi * (2 * k - 1) / (2 * n)))

    n1 = int(np.sqrt(n_col))
    xs = cheb_nodes_1d(n1); ts = cheb_nodes_1d(n1)
    X, T = np.meshgrid(xs, ts, indexing="xy")
    x = X.ravel()[:n_col]; t = T.ravel()[:n_col]

    if jitter > 0:
        x = np.clip(x + np.random.uniform(-jitter, jitter, size=x.shape), 0, 1)
        t = np.clip(t + np.random.uniform(-jitter, jitter, size=t.shape), 0, 1)

    x = torch.tensor(x, dtype=torch.float32, device=DEVICE).view(-1,1)
    t = torch.tensor(t, dtype=torch.float32, device=DEVICE).view(-1,1)
    return x, t

