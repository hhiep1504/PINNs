@torch.no_grad()
def predict_bagging(members, X):
    xt = torch.tensor(X, dtype=torch.float32, device=DEVICE)
    preds = [m(xt).cpu().numpy().ravel() for m in members]
    return np.mean(np.stack(preds, 0), 0)

def predict_uq_bagging(members, X_np):
    Xt = torch.tensor(X_np, dtype=torch.float32, device=DEVICE)
    uq_members = [TestTimeBernoulliUQ(m.net).to(DEVICE) for m in members]
    bagged = []
    for _ in range(T_MC):
        preds_m = []
        for uq in uq_members:
            uq.set_p(random.uniform(*P_RANGE_TEST))
            with torch.no_grad():
                preds_m.append(uq(Xt).cpu().numpy().ravel())
        bagged.append(np.mean(np.stack(preds_m, 0), 0))
    S = np.stack(bagged, 0)
    return S.mean(0), S.std(0, ddof=0)


def picp(mu, sd, y_true):
    lo, hi = mu - 1.96 * sd, mu + 1.96 * sd
    yt = y_true.ravel()
    return float(np.mean((yt >= lo) & (yt <= hi))) * 100

def mpiw(sd):
    return 2 * 1.96 * float(np.mean(sd))

def main():

    Xsup, ysup = make_supervised_grid_dynamic_impulse()
    Xva, yva = Xsup, ysup


    _, _, _, _, grid_test, y_exact = make_clean_test_grid()

    M_LIST = [1,2,5,10,20,50,100,200]
    bagging_members = []
    total_time = 0.0

    print("=== KHẢO SÁT ĐỘ NHẠY M - BAGGING PINN (VÍ DỤ 2) ===")
    print(f"{'M':<5} | {'Thời gian(s)':<13} | {'MAE (Det)':<10} | {'RMSE (Det)':<11} | {'PICP (%)':<10} | {'MPIW'}")
    print("-" * 80)

    for target_m in M_LIST:
        start_time = time.time()


        while len(bagging_members) < target_m:
            X_b, y_b = bootstrap(Xsup, ysup)

            x_c_b, t_c_b = make_collocation_fixed(1000, jitter=1e-3)
            trained_model = train_member(X_b, y_b, Xva, yva, x_c_b, t_c_b)
            bagging_members.append(trained_model)

        total_time += (time.time() - start_time)

        # Deterministic
        y_pred_det = predict_bagging(bagging_members, grid_test)
        mae = float(np.mean(np.abs(y_pred_det - y_exact)))
        rmse_det = float(np.sqrt(np.mean((y_pred_det - y_exact)**2)))

        #  PICP và MPIW
        y_mean_uq, y_sd_uq = predict_uq_bagging(bagging_members, grid_test)
        cr_val = picp(y_mean_uq, y_sd_uq, y_exact)
        mpiw_val = mpiw(y_sd_uq)

        print(f"{target_m:<5} | {total_time:<13.2f} | {mae:<10.6f} | {rmse_det:<11.6f} | {cr_val:>6.2f}     | {mpiw_val:.6f}")

if __name__ == "__main__":
    main()

